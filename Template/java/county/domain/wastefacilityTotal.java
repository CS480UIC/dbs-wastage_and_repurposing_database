package county.domain;

public class wastefacilityTotal {
	private String countyname;
	private Integer totalwaste;
	/**
	 * @return the countyname
	 */
	public String getCountyname() {
		return countyname;
	}
	/**
	 * @param countyname the countyname to set
	 */
	public void setCountyname(String countyname) {
		this.countyname = countyname;
	}
	/**
	 * @return the totalwaste
	 */
	public Integer getTotalwaste() {
		return totalwaste;
	}
	/**
	 * @param totalwaste the totalwaste to set
	 */
	public void setTotalwaste(Integer totalwaste) {
		this.totalwaste = totalwaste;
	}

}
